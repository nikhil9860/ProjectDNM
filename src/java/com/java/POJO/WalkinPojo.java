public class WalkinPojo {
    
    private String patient_name;
    private String patient_contact_no;
    private String gender;
    private String DOB;
    private String patient_address;
    private String appointment_time;
    private String appointment_date;

    /**
     * @return the patient_name
     */
    public String getPatient_name() {
        return patient_name;
    }

    /**
     * @param patient_name the patient_name to set
     */
    public void setPatient_name(String patient_name) {
        this.patient_name = patient_name;
    }

    /**
     * @return the patient_contact_no
     */
    public String getPatient_contact_no() {
        return patient_contact_no;
    }

    /**
     * @param patient_contact_no the patient_contact_no to set
     */
    public void setPatient_contact_no(String patient_contact_no) {
        this.patient_contact_no = patient_contact_no;
    }

    /**
     * @return the gender
     */
    public String getGender() {
        return gender;
    }

    /**
     * @param gender the gender to set
     */
    public void setGender(String gender) {
        this.gender = gender;
    }

    /**
     * @return the DOB
     */
    public String getDOB() {
        return DOB;
    }

    /**
     * @param DOB the DOB to set
     */
    public void setDOB(String DOB) {
        this.DOB = DOB;
    }

    /**
     * @return the patient_address
     */
    public String getPatient_address() {
        return patient_address;
    }

    /**
     * @param patient_address the patient_address to set
     */
    public void setPatient_address(String patient_address) {
        this.patient_address = patient_address;
    }

    /**
     * @return the appointment_time
     */
    public String getAppointment_time() {
        return appointment_time;
    }

    /**
     * @param appointment_time the appointment_time to set
     */
    public void setAppointment_time(String appointment_time) {
        this.appointment_time = appointment_time;
    }

    /**
     * @return the appointment_date
     */
    public String getAppointment_date() {
        return appointment_date;
    }

    /**
     * @param appointment_date the appointment_date to set
     */
    public void setAppointment_date(String appointment_date) {
        this.appointment_date = appointment_date;
    }
    
}
